package Loops;

//Print numbers from 10 to 1 in reverse order using a for loop.

public class Code3 {

    public static void main(String[] args) {

        // Declare variable
        int num = 10;

        // Itearate over in reverse order
        for (int i = num; i >= 1; i--) {
            System.out.println("i:" + i);
        }
    }
}
